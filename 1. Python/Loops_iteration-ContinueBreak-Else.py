# Loops and iteration - Continue and Break
# Else

i = 1
while i < 10:
    print(i)
    i += 1
else:
    print("Finally finished!, i =", i)
     
print("""
      
      """)

for i in range(1, 10):
    print(i)
else:
    print("Finally finished!, i =", i)

print("""
      
      """)

for i in range(1, 10):
    print(i)
    break
else:
    print("Finally finished!, i =", i)